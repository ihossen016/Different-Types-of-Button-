// On/Off Button Functions
function btn1() {
  // Color Toggle
  let element = document.getElementById('btn1')
  element.classList.toggle('toggle')

  // Text Toggle
  if (element.innerHTML === 'Off') {
    element.innerHTML = 'On'
  } else {
    element.innerHTML = 'Off'
  }
}
